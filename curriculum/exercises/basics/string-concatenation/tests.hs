module Main where

import System.Exit (exitFailure, exitSuccess)
import StringConcatenation

assertEqual :: (Show a, Eq a) => String -> a -> a -> IO Bool
assertEqual lbl got want
  | got == want = do
      putStrLn $ "  PASS: " ++ lbl
      return True
  | otherwise = do
      putStrLn $ "  FAIL: " ++ lbl
      putStrLn $ "    expected: " ++ show want
      putStrLn $ "    got:      " ++ show got
      return False

main :: IO ()
main = do
  results <- sequence
    [ assertEqual "greets Alice" (greetName "Alice") ("Hello, Alice!")
    , assertEqual "greets World" (greetName "World") ("Hello, World!")
    , assertEqual "greets an empty string" (greetName "") ("Hello, !")
    ]
  let passed = length (filter id results)
      failed  = length results - passed
  putStrLn $ show (length results) ++ " examples, " ++ show failed ++ " failures"
  if failed == 0 then exitSuccess else exitFailure
